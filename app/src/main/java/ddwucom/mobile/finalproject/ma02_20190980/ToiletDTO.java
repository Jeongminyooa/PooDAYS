package ddwucom.mobile.finalproject.ma02_20190980;

public class ToiletDTO {
    public String dataTitle;
    public String manTel;
    public String mw;
    public String openTime;
    public double posy;
    public double posx;
    public String toiletArea;

    public String getDataTitle() {
        return dataTitle;
    }

    public void setDataTitle(String dataTitle) {
        this.dataTitle = dataTitle;
    }

    public String getManTel() {
        return manTel;
    }

    public void setManTel(String manTel) {
        this.manTel = manTel;
    }

    public String getMw() {
        return mw;
    }

    public void setMw(String mw) {
        this.mw = mw;
    }

    public String getOpenTime() {
        return openTime;
    }

    public void setOpenTime(String openTime) {
        this.openTime = openTime;
    }

    public double getPosy() {
        return posy;
    }

    public void setPosy(double posy) {
        this.posy = posy;
    }

    public double getPosx() {
        return posx;
    }

    public void setPosx(double posx) {
        this.posx = posx;
    }

    public String getToiletArea() {
        return toiletArea;
    }

    public void setToiletArea(String toiletArea) {
        this.toiletArea = toiletArea;
    }
}
